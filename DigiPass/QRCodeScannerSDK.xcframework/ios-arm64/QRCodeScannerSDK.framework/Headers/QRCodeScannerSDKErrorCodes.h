// Indicates that an internal error occurred.
#define QRCodeScannerSDKErrorCodes_INTERNAL_ERROR -4500

// Indicates that the input parameter is null.
#define QRCodeScannerSDKErrorCodes_INPUT_PARAMETER_NULL -4501

// Indicates that no camera is available to perform the scanning process.
#define QRCodeScannerSDKErrorCodes_CAMERA_NOT_AVAILABLE -4502

// Indicates that the permission to access to the camera is denied.
#define QRCodeScannerSDKErrorCodes_PERMISSION_DENIED -4503

// Indicates that the provided image is invalid and cannot be decoded.
#define QRCodeScannerSDKErrorCodes_INVALID_IMAGE -4505

// Indicates that the provided image is too big to process.
#define QRCodeScannerSDKErrorCodes_IMAGE_TOO_BIG -4506

// Indicates that the provided code type is invalid.
#define QRCodeScannerSDKErrorCodes_INVALID_CODE_TYPE -4507
